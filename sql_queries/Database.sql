-- SQL Queries 
-- Crear tabla CLIENTES
CREATE TABLE CLIENTES (
    CLICOD VARCHAR(100) PRIMARY KEY,
    CLINOM VARCHAR(255),
    CLIDIR VARCHAR(255),
    CLICD VARCHAR(100),
    CLIEDO VARCHAR(100),
    CLISTA VARCHAR(100),
    CLICREDIT DECIMAL(10, 2),
    CLIPLAZOO INT,
    CLIDIREV INT,
    CLISACT DECIMAL(10, 2),
    CLIRFC VARCHAR(100),
    CLIALTA DATE,
    CLIOBS TEXT,
    CLICTA VARCHAR(100)
);

-- Crear tabla INVENTARIOS
CREATE TABLE INVENTARIOS (
    ICOD VARCHAR(100) PRIMARY KEY,
    IDESCR VARCHAR(255),
    ITIPO VARCHAR(100),
    ILISTA3 DECIMAL(10, 2),
    IFAM1 VARCHAR(100),
    IFAM2 VARCHAR(100),
    IFAM3 VARCHAR(100),
    IFAM4 VARCHAR(100),
    IFAM5 VARCHAR(100),
    IFAM6 VARCHAR(100),
    IMINIMO INT,
    IMAXIMO INT,
    ITIEMPO INT
);

-- Crear tabla DOCUMENTOS
CREATE TABLE DOCUMENTOS (
    DNUM INT PRIMARY KEY,
    DCANT DECIMAL(10, 2),
    DFECHA DATE,
    DVENCE DATE,
    DIVA DECIMAL(10, 2),
    DDESC DECIMAL(10, 2),
    DBRUTO DECIMAL(10, 2),
    DCANTE DECIMAL(10, 2),
    DREFER VARCHAR(100),
    DREFERELLOS VARCHAR(100),
    DESCXC INT,
    DESINV INT,
    DESFACT VARCHAR(100),
    DPAR1 VARCHAR(100),
    DPAR3 DECIMAL(10, 2),
    CLICOD VARCHAR(100),
    FOREIGN KEY (CLICOD) REFERENCES CLIENTES(CLICOD)
);

-- Crear tabla AUXILIAR_INVENTARIOS
CREATE TABLE AUXILIAR_INVENTARIOS (
    ICOD VARCHAR(100),
    FMOV VARCHAR(100),
    AITIPMV VARCHAR(100),
    AIPRECIO DECIMAL(10, 2),
    AICOSTO DECIMAL(10, 2),
    AIPREBR DECIMAL(10, 2),
    AIPZAS INT,
    AIDESCTO DECIMAL(10, 2),
    AICANTE INT,
    AIMES INT,
    AIALMACEN VARCHAR(100),
    AIPED VARCHAR(100),
    AIUSEQ VARCHAR(100),
    PRIMARY KEY (ICOD, FMOV),
    FOREIGN KEY (ICOD) REFERENCES INVENTARIOS(ICOD)
);

